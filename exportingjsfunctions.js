//ES6 Feature - Javascript Modules -export
export function add(a,b)
{
    //export - to call function add in other javascript
    return a+b;
}
export function sub(a,b)
{
    return a-b;
}
export function mul(a,b)
{
    return a*b;
}
export function div(a,b)
{
    return a/b;
}
export let welcomeMessage='Hello!!Welcome'//exporting a variable 'welcome message'